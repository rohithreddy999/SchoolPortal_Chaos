def _migration_002_tokenize_legacy_aadhaar(connection: Connection) -> None:
    if "students" not in _table_names(connection):
        return

    _add_column_if_missing(connection, "students", "date_of_birth", "date_of_birth DATE")
    _add_column_if_missing(
        connection,
        "students",
        "student_aadhaar_token",
        "student_aadhaar_token VARCHAR(64)",
    )
    _add_column_if_missing(
        connection,
        "students",
        "father_aadhaar_token",
        "father_aadhaar_token VARCHAR(64)",
    )

    columns = _column_names(connection, "students")
    if {"student_aadhaar", "father_aadhaar"} & columns:
        select_columns = ["id"]
        if "student_aadhaar" in columns:
            select_columns.append("student_aadhaar")
        if "father_aadhaar" in columns:
            select_columns.append("father_aadhaar")

        rows = connection.execute(text(f"SELECT {', '.join(select_columns)} FROM students")).mappings()
        for row in rows:
            values: dict[str, str | int | None] = {"id": row["id"]}
            assignments: list[str] = []
            if "student_aadhaar" in row.keys() and row["student_aadhaar"] is not None:
                student_aadhaar_token = tokenize_legacy_aadhaar(row["student_aadhaar"])
                if student_aadhaar_token is not None:
                    values["student_aadhaar_token"] = student_aadhaar_token
                    assignments.append("student_aadhaar_token = :student_aadhaar_token")
            if "father_aadhaar" in row.keys() and row["father_aadhaar"] is not None:
                father_aadhaar_token = tokenize_legacy_aadhaar(row["father_aadhaar"])
                if father_aadhaar_token is not None:
                    values["father_aadhaar_token"] = father_aadhaar_token
                    assignments.append("father_aadhaar_token = :father_aadhaar_token")
            if assignments:
                connection.execute(
                    text(f"UPDATE students SET {', '.join(assignments)} WHERE id = :id"),
                    values,
                )

    _drop_column_if_exists(connection, "students", "student_aadhaar")
    _drop_column_if_exists(connection, "students", "father_aadhaar")
    _exec(
        connection,
        "CREATE INDEX IF NOT EXISTS ix_students_student_aadhaar_token "
        "ON students (student_aadhaar_token)",
    )


MIGRATIONS: tuple[Migration, ...] = (
    Migration("001", "initial_schema", _migration_001_initial_schema),
    Migration("002", "tokenize_legacy_aadhaar", _migration_002_tokenize_legacy_aadhaar),
)


def apply_migrations(engine: Engine) -> None:
    with engine.begin() as connection:
        _create_schema_migrations(connection)
        applied = {
            row[0]
            for row in connection.execute(text("SELECT version FROM schema_migrations")).all()
        }

        for migration in MIGRATIONS:
            if migration.version in applied:
                continue
            migration.up(connection)
            connection.execute(
                text(
                    "INSERT INTO schema_migrations (version, name) "
                    "VALUES (:version, :name)"
                ),
                {"version": migration.version, "name": migration.name},
            )
