from __future__ import annotations

from collections.abc import Callable
from dataclasses import dataclass

from sqlalchemy import inspect, text
from sqlalchemy.engine import Connection, Engine

from app.core.aadhaar import tokenize_legacy_aadhaar


@dataclass(frozen=True)
class Migration:
    version: str
    name: str
    up: Callable[[Connection], None]


def _identity_pk(connection: Connection) -> str:
    if connection.dialect.name == "postgresql":
        return "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY"
    return "INTEGER PRIMARY KEY AUTOINCREMENT"


def _bool_default_false(connection: Connection) -> str:
    if connection.dialect.name == "postgresql":
        return "BOOLEAN NOT NULL DEFAULT FALSE"
    return "BOOLEAN NOT NULL DEFAULT 0"


def _exec(connection: Connection, sql: str) -> None:
    connection.execute(text(sql))


def _table_names(connection: Connection) -> set[str]:
    return set(inspect(connection).get_table_names())


def _column_names(connection: Connection, table_name: str) -> set[str]:
    if table_name not in _table_names(connection):
        return set()
    return {column["name"] for column in inspect(connection).get_columns(table_name)}


def _add_column_if_missing(
    connection: Connection,
    table_name: str,
    column_name: str,
    column_definition: str,
) -> None:
    if column_name in _column_names(connection, table_name):
        return
    _exec(connection, f"ALTER TABLE {table_name} ADD COLUMN {column_definition}")


def _drop_column_if_exists(connection: Connection, table_name: str, column_name: str) -> None:
    if column_name not in _column_names(connection, table_name):
        return
    _exec(connection, f"ALTER TABLE {table_name} DROP COLUMN {column_name}")


def _create_schema_migrations(connection: Connection) -> None:
    _exec(
        connection,
        """
        CREATE TABLE IF NOT EXISTS schema_migrations (
            version VARCHAR(20) PRIMARY KEY,
            name VARCHAR(100) NOT NULL,
            applied_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
        )
        """,
    )


